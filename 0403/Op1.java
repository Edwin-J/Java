package op;

public class Op1 {

	public static void main(String[] args) {
		int n1 = 7;
		int n2 = 4;
		
		int add = n1 + n2;
		int sub = n1 - n2;
		int mul = n1 * n2;
		int div = n1 / n2;
		int rem = n1 % n2;
	
		System.out.println("n1 + n2 = " + add);
		System.out.println("n1 - n2 = " + sub);
		System.out.println("n1 * n2 = " + mul);
		System.out.println("n1 / n2 = " + div);
		System.out.println("n1 % n2 = " + rem);
	}

}
